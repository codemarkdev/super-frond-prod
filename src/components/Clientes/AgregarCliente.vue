<template>
    <section>
        <p class="title is-1">Agregar cliente</p>
        <b-breadcrumb align="is-left">
            <b-breadcrumb-item tag='router-link' to="/">Inicio</b-breadcrumb-item>
            <b-breadcrumb-item tag='router-link' to="/clientes">Clientes</b-breadcrumb-item>
            <b-breadcrumb-item active>Agregar cliente</b-breadcrumb-item>
        </b-breadcrumb>
        <form-cliente :cliente="datosCliente" @registrar="onRegistrar" />
        <b-loading :is-full-page="true" v-model="cargando" :can-cancel="false"></b-loading>
    </section>
</template>
<script>
import apiRequest from '@/Servicios/HttpService';
import FormCliente from './FormCliente'
export default {
    name: "AgregarCliente",
    components: { FormCliente },

    data: () => ({
        cargando: false,
        datosCliente: {
            name: "",
            phone: ""
        }
    }),

    methods: {
        onRegistrar(cliente) {
            this.cargando = true
            apiRequest({
                method: 'POST',
                path: 'customers',
                data: cliente
            })
                .then(registrado => {
                    if (registrado) {
                        this.$buefy.toast.open({
                            type: 'is-info',
                            message: 'Cliente registrado con Ã©xito.'
                        })
                        this.cargando = false
                    }
                })
        }
    }
}
</script>